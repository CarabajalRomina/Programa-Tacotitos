    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
      
        //controlo si la tabla no esta vacia con el metodo getRowCount que me devuelve la cant de filas 
        if(tblTipo.getRowCount() > 0){
            //controlo si esta una fila seleccionada
            if(tblTipo.getSelectedRow() != -1){
                //capturo el id de la fila seleccionada
                //todos los datos cuando se cargan en una tabla son string 
                //le digo de la tabla tipo traeme el valor que esta en la columna 0 de la fila seleccionada, lo convierto en string y luego en entero
                int id_tipo = Integer.parseInt(String.valueOf(tblTipo.getValueAt(tblTipo.getSelectedRow(),0)));
                
                //llamo a la controladora de la logica 
                control.borrarTipo(id_tipo);
                
                //aviso si se borro correctamente 
                mostrarMensaje("Tipo de ingrediente eliminado correctamente", "info", "Eliminacion exitosa");
                //vuelvo a llamar al metodo cargar tabla para mostrarla actualizada despues de cada cambio
                cargarTabla();
        
               
            }
            //si no hay nada seleccionado aviso que no se pudo borrar 
            else{
                mostrarMensaje("No se selecciono ningun tipo de ingrediente","Error","Error al eliminar");
            }
            
        }
         //si no hay nada en la tabla aviso que no se pudo borrar 
        else{
            mostrarMensaje("No hay elementos para eliminar en la tabla","Error","Error al eliminar");
        }
        
       
    }//GEN-LAST:event_btnEliminarActionPerformed

