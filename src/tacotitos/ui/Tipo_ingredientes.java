    private void cargarTabla() {
        
        //definir el modelo que queremos que tenga la table, a traves de la clase defaul tablemodel
        //esta clase nos permite establecer un modelo a nuestra tabla 
        
        //hacemos que no se pueda editar el contenido de la tabla, todo el bloque pertenece a la instancia de tabla:
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            //del defaultTableModel que cree que se llama tabla hace que todas las celdas sean 
            //filas o columnas no se puedan editar, que sean fijas
            @Override
            public boolean isCellEditable(int row,int colum){
                return false;
            }
        };//pongo punto y como porque todo lo anterior forma parte de la instancia de la tabla 
        
        
        //establecemos los titulos de las columnas 
        
        String titulos[] = {"Id", "Descripcion", "Cantidad maxima"};
        //setamos los titulos en la tabla
        modeloTabla.setColumnIdentifiers(titulos);
        
        //carga de los datos desde la base de dato, declaro una lista y le asigno lo que me trae el metodo de la controladora
         List <Tipo_Ingrediente> listTipo = control.traerTipo_Ingredientes();
        
         //recorrer y mostrar los elementos de la lista en la tabla
         // si mi lista no esta vacia entonces:
         if(listTipo != null){
             //por cada tipo de ingrediente en la lista tipo cargamelas en la tabla
             for(Tipo_Ingrediente tipo : listTipo){
                 
                 //vamos a crear un array de tipo objeto
                 Object[] objTipo = { tipo.getId_tipo(), tipo.getDescripcion(), tipo.getCantMax()};
                 
                 //agregamos en una fila el objeto tipo
                 modeloTabla.addRow(objTipo);
             }
             
         }
         
         //le asigno el modelo de la tabla a mi tabla
         tblTipo.setModel(modeloTabla);
         
    
    
    }
    
   
